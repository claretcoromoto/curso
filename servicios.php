						      <p>Son muy pocas las aplicaciones en la actualidad que no lleven persistencia de datos a través de algún gestor de bases de datos (GBD). Pero una mala definición en la estructura de la base de datos (BD), o de sus procedimientos almacenados y funciones, pueden traer como consecuencia: crecimiento innecesario de la BD en el disco duro,
